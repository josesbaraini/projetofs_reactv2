'use client'
import styles from './Svg.modules.css'
import { useRouter } from 'next/navigation'
import { useState, useEffect } from 'react'
import { useUser } from '@/components/UserContext';
import apiRoutes from '@/utils/apiRoutes';

export default function Svg(props) {
    const [iconColor, setIconColor] = useState("");
    const [fotoPerfil, setFotoPerfil] = useState(null);
    const [erroFoto, setErroFoto] = useState(false);
    const { usuario } = useUser();

    useEffect(() => {
        const color = getComputedStyle(document.documentElement)
            .getPropertyValue("--texto")
            .trim();
        setIconColor(color);
    }, []);

    // Buscar foto do perfil quando o componente for do tipo 'user'
    useEffect(() => {
        if (props.tipo === 'user' && usuario?.id) {
            buscarFotoPerfil();
        }
    }, [props.tipo, usuario?.id]);

    const buscarFotoPerfil = async () => {
        try {
            const response = await fetch(apiRoutes.fotoPerfil(usuario.id), {
                method: 'GET',
                credentials: 'include'
            });



            if (response.ok) {
                const blob = await response.blob();


                const imageUrl = URL.createObjectURL(blob);


                setFotoPerfil(imageUrl);
                setErroFoto(false);
            } else {
    
                setErroFoto(true);
            }
        } catch (error) {
            console.error('Erro ao buscar foto do perfil:', error);
            setErroFoto(true);
        }
    };

    // Limpar URL do blob quando o componente for desmontado
    useEffect(() => {
        return () => {
            if (fotoPerfil) {
                URL.revokeObjectURL(fotoPerfil);
            }
        };
    }, [fotoPerfil]);

    switch (props.tipo) {
        case 'user':
            // Se tem foto do perfil e não houve erro, mostra a foto
            if (fotoPerfil && !erroFoto) {
                return (
                    <img
                        src={fotoPerfil}
                        alt="Foto do perfil"
                        width={props.largura}
                        height={props.altura}
                        style={{
                            borderRadius: '50%',
                            objectFit: 'cover',
                            cursor: props.funcao ? 'pointer' : 'default'
                        }}
                        onClick={props.funcao}
                        className={styles.icone}
                    />
                );
            }
            // Se não tem foto ou houve erro, mostra o SVG padrão
            return (<svg width={props.largura} onClick={props.funcao} className={styles.icone} height={props.altura} viewBox="-2.4 -2.4 28.80 28.80" fill="none" xmlns="http://www.w3.org/2000/svg"><g id="SVGRepo_bgCarrier" strokeWidth="0"></g><g id="SVGRepo_tracerCarrier" strokeLinecap="round" strokeLinejoin="round"></g><g id="SVGRepo_iconCarrier"> <path d="M5 21C5 17.134 8.13401 14 12 14C15.866 14 19 17.134 19 21M16 7C16 9.20914 14.2091 11 12 11C9.79086 11 8 9.20914 8 7C8 4.79086 9.79086 3 12 3C14.2091 3 16 4.79086 16 7Z" stroke='#000000' strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"></path> </g></svg>)
        case 'userU':
            return (<svg width="25px" height="25px" viewBox="-2.4 -2.4 28.80 28.80" fill="none" xmlns="http://www.w3.org/2000/svg"><g id="SVGRepo_bgCarrier" strokeWidth="0"></g><g id="SVGRepo_tracerCarrier" strokeLinecap="round" strokeLinejoin="round"></g><g id="SVGRepo_iconCarrier"> <path d="M5 21C5 17.134 8.13401 14 12 14C15.866 14 19 17.134 19 21M16 7C16 9.20914 14.2091 11 12 11C9.79086 11 8 9.20914 8 7C8 4.79086 9.79086 3 12 3C14.2091 3 16 4.79086 16 7Z" stroke={iconColor} strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"></path> </g></svg>)
        case 'pesquisa':
            return (<svg width="20px" height="20px" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg"><g id="SVGRepo_bgCarrier" strokeWidth="0"></g><g id="SVGRepo_tracerCarrier" strokeLinecap="round" strokeLinejoin="round"></g><g id="SVGRepo_iconCarrier"><path d="M15.5 15.5L19 19M5 11C5 7.68629 7.68629 5 11 5C14.3137 5 17 7.68629 17 11C17 14.3137 14.3137 17 11 17C7.68629 17 5 14.3137 5 11Z" stroke={iconColor} strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" /></g></svg>)
        case 'notificacao':
            return (<svg width="50px" className={styles.icone} height="50px" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg"><g id="SVGRepo_bgCarrier" strokeWidth="0"></g><g id="SVGRepo_tracerCarrier" strokeLinecap="round" strokeLinejoin="round"></g><g id="SVGRepo_iconCarrier"><path d="M10.146 3.248a2 2 0 0 1 3.708 0A7.003 7.003 0 0 1 19 10v4.697l1.832 2.748A1 1 0 0 1 20 19h-4.535a3.501 3.501 0 0 1-6.93 0H4a1 1 0 0 1-.832-1.555L5 14.697V10c0-3.224 2.18-5.94 5.146-6.752zM10.586 19a1.5 1.5 0 0 0 2.829 0h-2.83zM12 5a5 5 0 0 0-5 5v5a1 1 0 0 1-.168.555L5.869 17H18.13l-.963-1.445A1 1 0 0 1 17 15v-5a5 5 0 0 0-5-5z" fill="#FFFFFF"></path></g></svg>)
        case 'logout':
            return (<svg width="25px" height="25px" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg"><g id="SVGRepo_bgCarrier" strokeWidth="0"></g><g id="SVGRepo_tracerCarrier" strokeLinecap="round" strokeLinejoin="round"></g><g id="SVGRepo_iconCarrier"> <path d="M12.9999 2C10.2385 2 7.99991 4.23858 7.99991 7C7.99991 7.55228 8.44762 8 8.99991 8C9.55219 8 9.99991 7.55228 9.99991 7C9.99991 5.34315 11.3431 4 12.9999 4H16.9999C18.6568 4 19.9999 5.34315 19.9999 7V17C19.9999 18.6569 18.6568 20 16.9999 20H12.9999C11.3431 20 9.99991 18.6569 9.99991 17C9.99991 16.4477 9.55219 16 8.99991 16C8.44762 16 7.99991 16.4477 7.99991 17C7.99991 19.7614 10.2385 22 12.9999 22H16.9999C19.7613 22 21.9999 19.7614 21.9999 17V7C21.9999 4.23858 19.7613 2 16.9999 2H12.9999Z" fill={iconColor}></path> <path d="M13.9999 11C14.5522 11 14.9999 11.4477 14.9999 12C14.9999 12.5523 14.5522 13 13.9999 13V11Z" fill={iconColor}></path> <path d="M5.71783 11C5.80685 10.8902 5.89214 10.7837 5.97282 10.682C6.21831 10.3723 6.42615 10.1004 6.57291 9.90549C6.64636 9.80795 6.70468 9.72946 6.74495 9.67492L6.79152 9.61162L6.804 9.59454L6.80842 9.58848C6.80846 9.58842 6.80892 9.58778 5.99991 9L6.80842 9.58848C7.13304 9.14167 7.0345 8.51561 6.58769 8.19098C6.14091 7.86637 5.51558 7.9654 5.19094 8.41215L5.18812 8.41602L5.17788 8.43002L5.13612 8.48679C5.09918 8.53682 5.04456 8.61033 4.97516 8.7025C4.83623 8.88702 4.63874 9.14542 4.40567 9.43937C3.93443 10.0337 3.33759 10.7481 2.7928 11.2929L2.08569 12L2.7928 12.7071C3.33759 13.2519 3.93443 13.9663 4.40567 14.5606C4.63874 14.8546 4.83623 15.113 4.97516 15.2975C5.04456 15.3897 5.09918 15.4632 5.13612 15.5132L5.17788 15.57L5.18812 15.584L5.19045 15.5872C5.51509 16.0339 6.14091 16.1336 6.58769 15.809C7.0345 15.4844 7.13355 14.859 6.80892 14.4122L5.99991 15C6.80892 14.4122 6.80897 14.4123 6.80892 14.4122L6.804 14.4055L6.79152 14.3884L6.74495 14.3251C6.70468 14.2705 6.64636 14.1921 6.57291 14.0945C6.42615 13.8996 6.21831 13.6277 5.97282 13.318C5.89214 13.2163 5.80685 13.1098 5.71783 13H13.9999V11H5.71783Z" fill={iconColor}></path> </g></svg>)
        case 'chenge':
            return (<svg width="25px" height="25px" fill={iconColor} viewBox="0 0 24 24" id="update-alt-2" data-name="Flat Color" xmlns="http://www.w3.org/2000/svg" ><g id="SVGRepo_bgCarrier" strokeWidth="0"></g><g id="SVGRepo_tracerCarrier" strokeLinecap="round" strokeLinejoin="round"></g><g id="SVGRepo_iconCarrier"><path id="primary" d="M21.71,10.29a1,1,0,0,0-1.42,0L19,11.59V7a3,3,0,0,0-3-3H6A1,1,0,0,0,6,6H16a1,1,0,0,1,1,1v4.59l-1.29-1.3a1,1,0,0,0-1.42,1.42l3,3a1,1,0,0,0,1.42,0l3-3A1,1,0,0,0,21.71,10.29Z"></path><path id="secondary" d="M18,18H8a1,1,0,0,1-1-1V12.41l1.29,1.3a1,1,0,0,0,1.42,0,1,1,0,0,0,0-1.42l-3-3a1,1,0,0,0-1.42,0l-3,3a1,1,0,0,0,1.42,1.42L5,12.41V17a3,3,0,0,0,3,3H18a1,1,0,0,0,0-2Z"></path></g></svg>)
        default:
            break;
    }


}
